// Random Number generator 

#include<stdio.h>
#include<stdlib.h>
#define ll long long

ll strSize(char* s) {
    ll size=0;
    for(ll i=0;s[i]!='\0';i++) {
        size++;
    }
    return size;
}

ll dig(ll n) {
    ll digits=0;
    while(n>0) {
        n/=10;
        digits++;
    }
    return digits;
}

int ith_digit(ll n, ll i) {
    if(i > dig(n)) {
        return -1;
    }
    ll fromBack = dig(n)-i;
    while(fromBack>0) {
        n/=10;
    }
    int ans = n%10+48;
    return ans;
}


ll toLL(char* s) {
    ll val = 1, ans = 0, n = strSize(s);
    for(ll i=n-1;i>=0;i--) {
        ans+=(s[i]-48)*val;
        val*=10;
    }
    return ans;
}

char* toStr(ll n) {
    ll len = dig(n);
    char* str = (char*) malloc(sizeof(char)*(len+1));
    ll end = len-1;
    while(n>0) {
        str[end--]=n%10+48;
        n/=10;
    }
    str[len]='\0';
    return (char *)str;
}

ll nextRand(char* seed,ll digits) {
    ll n=toLL(seed);
    n=n*n;
    if(n<0)return 0;
    for(ll i=0;i<digits/2+digits%2;i++) {
        n/=10;
    }
    for(ll i=0;i<digits;i++) {
        seed[digits-i-1]=n%10+48;
        n/=10;
    }
    return toLL(seed);
}

float nextRandfloat(char* seed,ll digits) {
    ll x = nextRand(seed,digits);
    ll div = 0, mul = 9;
    for(ll i=0;i<digits;i++) {
        div+=mul;
        mul*=10;
    }
    //printf("%lld\n",div);
    return x*1.000/div;
}

void printRand(char* seed,ll digits, ll ct) {
    for(ll i=0;i<ct;i++) {
        printf("__");
    }
    printf("\n");
    for(ll i=0;i<ct;i++) {
        printf("|");
        for(ll j=0;j<ct;j++) {
            float f=nextRandfloat(seed,digits); 
            if(f>0.5) {
                printf("<>");
            }
            else printf("  ");
        }
        printf("|\n");
    }
    for(ll i=0;i<ct;i++) {
        printf("__");
    }
    printf("\n");
    printf("end\n");
}

int main() {
    ll digits, ct;
    scanf("%lld%lld",&digits,&ct);
    char seed[digits+1];
    scanf("%s",seed);
    printRand(seed,digits,ct);
    return 0;
}


