// Random Number generator 

#include<iostream>
#include<string>
#define ll long long
using namespace std;

string seed;
ll digits;

ll nextRand() {
    ll n = stoi(seed);
    cout<<n;
}

int main() {
    cin>>digits;
    cin>>seed;
    nextRand();
}