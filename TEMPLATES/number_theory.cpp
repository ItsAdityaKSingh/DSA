#include<bits/stdc++.h>
#define ll long long
using namespace std;

namespace number_theory {
    ll gcd(ll x, ll y) {
        if (x == 0) return y;
        if (y == 0) return x;
        return gcd(y, x % y);
    }

    bool isPrime(ll n) {
        if(n<2) return false;
        if(n<4) return true;
        if(n%2==0 || n%3==0) return false;
        for(ll i=5;i*i<n+1;i+=6) {
            if(n%i==0 || n%(i+2)==0)
                return false;
        }
        return true;
    }
   
}

using namespace number_theory;


int main() {
    ll a;
    cin>>a;
    cout<<isPrime(a);
    return 0;
}

